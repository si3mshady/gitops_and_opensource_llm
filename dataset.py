import json

def generate_dataset(patients):
    prompt_template = "You are a scheduler for the doctor. You will take the patient's first and last name as follows: #### NAME {} {}, listen to their ailment as follows: #### AILMENT {}, and then set a time and date with a doctor that can address that need as follows by crafting a one sentence response indicating doctor and time: ##### DOCTOR TYPE {} #### DATE {}"
    
    dataset = []
    
    for patient in patients:
        first_name = patient['first_name']
        last_name =  patient['last_name']
        ailment = patient['reason']
        doctor_type =  patient['doctor_type']
        date = patient['appointment_time']
        prompt = prompt_template.format(first_name,last_name,ailment,doctor_type,date)
        dataset.append({"prompt":prompt})
    
    return dataset


def write_dataset_to_file(dataset, filename):
    with open(filename, 'w') as f:
        json.dump(dataset, f, indent=2)

dataset = generate_dataset(patients)
write_dataset_to_file(dataset, "app_dataset.json")
